#include <iostream>

int main() {
    int a = 10;
    int b = 20;
    // let us perform operations on these
    int c = a + b;
    int d = a - b;
    int e = a * b;
    int f = a / b;

    std::cout << "c = " << c << "\n";
    std::cout << "d = " << d << "\n";
    std::cout << "e = " << e << "\n";
    std::cout << "f = " << f << "\n";

    return 0;
}